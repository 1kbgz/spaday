def transports_panel(prefix: str, title: str) -> object:
    """One live chart + its controls, fully declarative against the model's ``{prefix}.*`` namespace in the
    shared store. Type/Live are two-way binds (inbound reflects the model onto the control, outbound sends a
    server edit via connectStore); the chart props and the status are ``compute``s; "Clear" is a SendPatch
    the generated page routes into ``{prefix}.data``. No hand-written reflect or series transform."""
    select = WaSelect(label="Type").prop("id", f"{prefix}-type").bind("value", f"{prefix}.type", mode="two-way")
    for t in TYPES:
        select = select.child(WaOption(value=t).text(t.capitalize()))
    # the wire block sets `{prefix}.connected` on the socket's open/close; the status reads it
    status = (
        element("span")
        .prop("id", f"{prefix}-status")
        .prop("style", "margin-left:auto;color:#2962ff;font-size:.8rem")
        .compute("textContent", cond(field(f"{prefix}.connected"), "live", "disconnected"))
    )
    return (
        Stack()
        .child(Row().child(element("strong").text(title)).child(status))
        .child(
            Toolbar()
            .child(select)
            .child(WaSwitch().prop("id", f"{prefix}-live").bind("checked", f"{prefix}.live", mode="two-way").text("Live"))
            .child(WaButton(variant="neutral").prop("id", f"{prefix}-clear").text("Clear").on("click", SendPatch(prefix, "data", lit({}))))
        )
        # the chart's type + series come from the model (the wrapper sorts the time-keyed map — no transform)
        .child(
            LightweightChart()
            .prop("id", f"{prefix}-chart")
            .prop("style", "height:260px;display:block")
            .compute("type", field(f"{prefix}.type"))
            .compute("data", field(f"{prefix}.data"))
            .compute("theme", DARK_THEME)
        )
    )


