"""The spaday omnibus example — the whole stack in one self-serving app.

Run it and open the URL::

    python -m spaday.examples        # -> http://127.0.0.1:8000

It shows, on one page authored entirely in Python:

- **Shell layout** — the page is composed from ``spa-*`` shell components
  (:mod:`spaday.components.shell`: ``App`` / ``Nav`` / ``Body`` / ``Gutter`` / ``Main`` / ``Footer`` with
  ``Stack`` / ``Row`` / ``Toolbar``), not raw ``div``s.
- **Action DSL (client-side)** — controls carry declarative actions (:mod:`spaday.actions` +
  :meth:`~spaday.component.Component.on`) interpreted in the browser: a Toggle, a SetProp bound to the
  event value, a Sequence, and buttons that switch a chart's series type. The page wires **no** event
  listeners and never calls the server for these.
- **transports (server-authoritative, multi-tenant)** — two live charts mirror Python models over
  ``@1kbgz/transports``. A **global** model on a shared :class:`transports.Server` syncs to every browser;
  a **per-session** model on a :class:`transports.Hub` is private to each tab. Their fields would collide
  (both are ``Chart`` with ``type``/``data``/``live``), so each is mirrored under its own **namespace**
  (``global.*`` / ``session.*``) into one signal store — the chart props and the controls are plain
  two-way binds + ``compute``s against those namespaced fields, and "Clear" is a declarative
  :class:`~spaday.actions.SendPatch` the generated page routes into the store.
- **Form from a schema** — a whole form generated by ``form(Device)`` from a pydantic model: an ``Enum``
  field, a ``FormField`` relabel, and a nested ``Schedule`` sub-model rendered as an expand/collapse
  ``wa-details`` whose controls bind to dotted ``schedule.start`` paths. Every field is two-way bound to
  a hosted model over transports through ``connectStore`` (a fourth, un-namespaced wire), so nested edits
  fan to other tabs.

There is **no hand-written HTML or page JS**: ``serve(page, wire=[…], store=…, background=[…], routes=[…])``
generates the whole bootstrap — the four transports wires (each namespaced ``connectStore``), the seeded
signal store, and the ``spaday:patch`` sink — and mounts the Python-authored tree.
"""

